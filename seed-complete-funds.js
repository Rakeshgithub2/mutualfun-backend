// Generate fund data with realistic variations
function generateFundData(fund, index) {
  const now = new Date();
  const navVariation = 1 + (Math.random() * 0.1 - 0.05); // Â±5% variation
  const currentNav = (fund.baseNav * navVariation).toFixed(2);
  const previousNav = (currentNav * 0.995).toFixed(2); // 0.5% lower

  const returnsVariation = 1 + (Math.random() * 0.2 - 0.1); // Â±10% variation
  const returns1Y = (fund.returns1Y * returnsVariation).toFixed(2);
  const returns3Y = (returns1Y * 0.82).toFixed(2); // Typically lower than 1Y
  const returns5Y = (returns1Y * 0.78).toFixed(2); // Even lower for 5Y

  return {
    _id: new ObjectId(),
    name: fund.name,
    category: fund.category,
    subCategory: fund.subCategory,
    fundType: fund.fundType || 'mutual_fund',
    fundHouse: fund.fundHouse,
    currentNav: parseFloat(currentNav),
    previousNav: parseFloat(previousNav),
    navDate: now,
    returns: {
      day: parseFloat((Math.random() * 2 - 0.5).toFixed(2)),
      week: parseFloat((Math.random() * 4 - 1).toFixed(2)),
      month: parseFloat((Math.random() * 8 - 2).toFixed(2)),
      threeMonth: parseFloat((returns1Y * 0.25).toFixed(2)),
      sixMonth: parseFloat((returns1Y * 0.5).toFixed(2)),
      oneYear: parseFloat(returns1Y),
      threeYear: parseFloat(returns3Y),
      fiveYear: parseFloat(returns5Y),
      sinceInception: parseFloat((returns5Y * 0.92).toFixed(2)),
    },
    riskMetrics: {
      sharpeRatio: parseFloat((1.5 + Math.random() * 1.2).toFixed(2)),
      standardDeviation: parseFloat((10 + Math.random() * 8).toFixed(2)),
      beta: parseFloat((0.8 + Math.random() * 0.6).toFixed(2)),
      alpha: parseFloat((Math.random() * 4).toFixed(2)),
      r2: parseFloat((0.7 + Math.random() * 0.25).toFixed(2)),
      treynorRatio: parseFloat((1.2 + Math.random() * 0.8).toFixed(2)),
    },
    aum: fund.aum,
    expenseRatio: fund.expenseRatio,
    ratings: {
      morningstar: fund.rating,
      valueResearch: Math.max(
        3,
        Math.min(5, fund.rating + (Math.random() > 0.5 ? 1 : -1))
      ),
      crisil: fund.rating,
    },
    minInvestment: fund.fundType === 'commodity' ? 100 : 5000,
    sipMinAmount: fund.fundType === 'commodity' ? 100 : 500,
    exitLoad:
      fund.fundType === 'commodity' ? '0%' : '1% (if redeemed before 1 year)',
    launchDate: new Date(
      2015 + Math.floor(Math.random() * 8),
      Math.floor(Math.random() * 12),
      1
    ),
    popularity: 1000 - index,
    tags: [fund.subCategory, fund.category.replace('_', ' '), 'Direct Plan'],
  };
}

